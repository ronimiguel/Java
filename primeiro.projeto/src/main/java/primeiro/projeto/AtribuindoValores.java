package primeiro.projeto;

public class AtribuindoValores {

	public static void main(String[] args) {

		int numeroInteiro;

		numeroInteiro = 2;
		System.out.println("O valor da variavel numeroInteiro é:\n" + numeroInteiro);

		numeroInteiro = 20;
		System.out.println("numeroInteiro mudou, agora ele tem o valor:\n" + numeroInteiro);

		numeroInteiro = 33;
		System.out.println("numeroInteiro mudou de novo, valor agora é\n" + numeroInteiro);

	}

}
