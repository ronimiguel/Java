package primeiro.projeto;

public class Multiplode400 {

	public static void main(String[] args) {
		
	int num = 800;
	
	System.out.println("Testando se valor de num = " + num + " é multiplo de 400");
	
	if (num % 400 == 0) {
		System.out.println(num + " é multiplo de 400");
	}else {
		System.out.println(num + " não é multiplo de 400");
	}
		
	}

}
